package stringClassMethods;

public class StartWithAndEndWith {

	//Java String startsWith() and endsWith() method

	
	public static void main(String[] args) {
		
		//The method startsWith() checks whether the String starts with the letters passed as arguments 
		
		// endsWith() method checks whether the String ends with the letters passed as arguments.

		String p="Pratik";
		
		System.out.println(p);
		
		System.out.println(p.startsWith("P"));
		
		System.out.println(p.endsWith("b"));
		
		
		
		
		

	}

}
