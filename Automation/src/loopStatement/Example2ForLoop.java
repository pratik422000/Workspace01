package loopStatement;

public class Example2ForLoop {

	// Java program to calculate sum of natural numbers till 10.
	
	public static void main(String []args) 
	{

		int n=10;
		int sum=0;
		int i = 0;
		for (i=1; i<=n; i++)
		{
			sum =sum+i;
			}
		System.out.println("The sum of naturals numbsers from 1 to 10 is"  + sum);
		}
	
	
	
	
	
	
	
	
}
