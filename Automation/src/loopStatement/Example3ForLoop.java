package loopStatement;

public class Example3ForLoop {

	// Java program to find factorial of 5. 1*2*3*4*5
	
	public static void main(String []args) 
	{

		int n=5;
		int f=1;
	
		for (int i=1; i<=n; i++)
		{
			f=f*i;
			
		}
		System.out.println("Factorial of 5 is" + f);
		
		}
	
	
	
	
	
	
	
	
	
}
