package statement;

public class Example8 {

	
	////Switch statement
	public static void main (String[]args)
	{
String city = "mumbai";
		
		switch (city)
		{
		
		case"mumbai":System.out.println("I'm from mumbai");
		break;
		case"pune":System.out.println("I'm from pune");
		break;
		case"Channai":System.out.println("I'm from channai");
		break;
		case"delhi":System.out.println("I'm from delhi");
		break;
		default:System.out.println("I'm from another city");
	
	
		}
	
	}	
	
}
